  document.addEventListener('DOMContentLoaded', function() {
    // JavaScript to add hover effect
    var trendingLink = document.getElementById('trendingLink');

    trendingLink.addEventListener('mouseover', function() {
      trendingLink.style.textDecoration = 'underline';
    });

    trendingLink.addEventListener('mouseout', function() {
      trendingLink.style.textDecoration = 'none';
    });
  });

